//Decimals or floating points

// Addition
var a = 2.0 + 3.4;
console.log(a); //Output 5.4

//Subtraction
var b = 2.0 - 3.4;
console.log(b); //Output -1.4

//Multiplication
var c = 2.0 * 3.4;
console.log(c); //Output 6.8
//Division
var d = 2.0 / 3.4;
console.log(d); //Output 0.588...

//Remainder operator  //Gives the remainder of the division of two numbers
var e = 11 % 3;
console.log(e); // Output 2
//It is often used to determine if a number is even or odd

//Compound assignment  with augmented addition
var f = 7;
f += 3;
console.log(f); //Output 10
/* This is a shorter way of writing f = f + 3
So we are adding the value to the variable, and assigning the answer to that variable
*/
//Compound assignment with augmented subtraction
var g = 7;
g -= 3;
console.log(g); //Output 4
//Compound assignment with augmented multiplication
var h = 7;
h *= 3;
console.log(h); //Output 21
//Compound assignment with augmented division
var i = 7;
i /= 2;
console.log(i); //Output 3.5
